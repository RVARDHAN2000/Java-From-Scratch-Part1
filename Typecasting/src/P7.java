// char to other
class  P7
{
	public static void main(String[] args) 
	{
		char a=65;
		System.out.println(a);
		byte b=(byte)a;
		System.out.println(b);
		short  c=(short)a;
		System.out.println(c);
		int d=(int)a;
		System.out.println(d);
		long e=(long)a;
		System.out.println(e);
		float f=(float)a;
		System.out.println(f);
		double g = (double)a;
		System.out.println(g);
	}
}
