// long to other
class  P4
{
	public static void main(String[] args) 
	{
		long a=65;
		System.out.println(a);
		byte b=(byte)a;
		System.out.println(b);
		short  c=(short)a;
		System.out.println(c);
		int d=(int)a;
		System.out.println(d);
		float e=(float)a;
		System.out.println(e);
		double f=a;
		System.out.println(f);
		char g = (char)a;
		System.out.println(g);
	}
}
