//WAJP to find sum and multiplication of two numbers Create individual method for summation and multiplication
class P3
{
	public static void main(String[] args) 
	{
		add(10,20);
	    mult(20,30);
	}
	public static void add (int a, int b)
	{
		int res=a+b;
		System.out.println("The addition of two given number is "+res);
	}
	public static void mult (int a, int b)
	{
		int res=a*b;
		System.out.println("The multiplication of two given number is "+res);
	}

}


/*The addition of two given number is 30
The multiplication of two given number is 600*/